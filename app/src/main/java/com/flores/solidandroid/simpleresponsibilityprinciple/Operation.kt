package com.flores.solidandroid.simpleresponsibilityprinciple

object Operation {

    fun add(numb1: Int, numb2: Int): Int {
        return numb1 + numb2
    }

    fun subtract(numb1: Int, numb2: Int): Int {
        return numb1 - numb2
    }

    fun multiply(numb1: Int, numb2: Int): Int {
        return numb1 * numb2
    }

    fun divide(numb1: Int, numb2: Int): Int {
        return numb1 / numb2
    }
}